panjang = 500
lebar = 5
tinggi = 10

volume = panjang * lebar * tinggi
print (f"Hasil perhitungan volume: {volume}cm^3")

keliling = 4 * (panjang + lebar + tinggi)
print (f"Hasil perhitungan keliling: {keliling}cm") 

luas = 2 * (panjang * lebar + panjang * tinggi + lebar * tinggi)
print (f"Hasil perhitungan luas: {luas}cm^2")
